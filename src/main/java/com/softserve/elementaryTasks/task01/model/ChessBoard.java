package com.softserve.elementaryTasks.task01.model;

public class ChessBoard {

    private int height;
    private int width;

    public ChessBoard(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
